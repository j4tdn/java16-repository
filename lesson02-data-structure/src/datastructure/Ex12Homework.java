package datastructure;
// Swap 2 Integer homework
public class Ex12Homework {
	public static void main(String[] args) {
		Integer a = 22;
		Integer b = 66;
		int temp  = 0;
		
		temp = a;
		a = b;
		b = temp;
		
		System.out.println("Integer a = " + a);
		System.out.println("Integer b = " + b);
	}
}
